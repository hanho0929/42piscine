int		ft_is_prime(int nb)
{
	int i;
	int flag;

	if (nb < 1)
		return (0);
	i = 1;
	flag = 0;
	while (i++ <= nb / 2)
		if (nb % i == 0)
		{
			flag = 1;
			break ;
		}
	if (flag == 0)
		return (1);
	return (0);
}
