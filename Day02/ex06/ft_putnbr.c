#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void ft_putnbr(int nb)
{
	int i;

	i = nb;
	ft_putchar(i + '0');
}

int main()
{
	ft_putnbr(20);
	return(0);
}
